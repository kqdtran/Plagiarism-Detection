public class P2PhoneBookEntry
{
    private String name;
    private int phoneNum;
    
    public P2PhoneBookEntry(String n, int p)
    {
        name = n;
        phoneNum = p;
    }
    
    public String getName()
    {
        return name;
    }
    
    public int getPhoneNum()
    {
        return phoneNum;
    }
    
}
   

